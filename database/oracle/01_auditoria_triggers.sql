CREATE OR REPLACE TRIGGER trg_aud_pacientes_insert
AFTER INSERT ON pacientes
FOR EACH ROW
BEGIN
    INSERT INTO auditoria_acciones (
        nombre_tabla,
        accion,
        valor_llave,
        usuario_bd,
        detalle
    )
    VALUES (
        'PACIENTES',
        'INSERT',
        TO_CHAR(:NEW.paciente_id),
        USER,
        'Se creó el paciente: ' || :NEW.nombre || ' ' || :NEW.apellido
    );
END;
