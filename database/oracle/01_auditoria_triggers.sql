PROMPT CREANDO TABLA DE AUDITORIA
PROMPT ============================================

BEGIN
    EXECUTE IMMEDIATE '
        CREATE TABLE auditoria_acciones (
            auditoria_id NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
            nombre_tabla VARCHAR2(50) NOT NULL,
            accion VARCHAR2(20) NOT NULL,
            valor_llave VARCHAR2(100),
            usuario_bd VARCHAR2(100) NOT NULL,
            fecha_accion TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
            detalle VARCHAR2(500),
            CONSTRAINT pk_auditoria_acciones PRIMARY KEY (auditoria_id)
        )
    ';

    DBMS_OUTPUT.PUT_LINE('Tabla AUDITORIA_ACCIONES creada correctamente.');

EXCEPTION
    WHEN OTHERS THEN
        IF SQLCODE = -955 THEN
            DBMS_OUTPUT.PUT_LINE('La tabla AUDITORIA_ACCIONES ya existe.');
        ELSE
            RAISE;
        END IF;
END;
/

